## These two functions produce a mechanism for solving for the inverse of an
## invertible square matrix, as well as for caching the inverse so that the 
## calculation need only be performed once.

## makeCacheMatrix() provides definitions for anonymous function calls 
## for four operations.: set, get, setinverse, and getinverse

makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinverse <- function(solve) m <<- solve
  getinverse <- function() m
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}


## cacheSolve() is designed to be an implementation of the anonymous
## function referenced in makeCacheMatrix()

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  m <- x$getinverse()
  if(!is.null(m)) {
    message("getting cached inverse")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setinverse(m)
  m
}
